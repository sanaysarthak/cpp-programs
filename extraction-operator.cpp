// Insertion operator is also used as Bitwise right-shift operator (>>)
#include <iostream>
using namespace std;
int main()
{
    int num1, num2;

    cout << "Enter First Number : ";
    cin >> num1;

    cout << "Enter Second Number : ";
    cin >> num2;

    cout << "Addition : ";
    cout << num1 + num2;

    return 0;
}
